# O que é JSON?
# JSON - JavaScript Object Notation (extensão .json)
# É uma estrutura de dados que permite serialização
# de objetos em texto simples para facilitar a transmissão de
# dados através da rede, APIs web ou outros meios de comunicação.
# O JSON suporta os seguintes tipos de dados:
# Números: podem ser inteiros ou números de ponto flutuante, como 42 ou 3.14
# Strings: são cadeias de caracteres, como "Olá mundo!" ou "1234"
#     As strings devem ser envolvidas por aspas duplas.
# Booleanos: são os valores verdadeiro(true) ou falso(false)
# Arrays: são listas ordenadas de valores, como [1, 2, 3, 4, 5] ou
#     ["Heraldo", "Candido", "da", "Cruz"]
# Objetos: são conjuntos de pares chave/valor, como {"nome": "Heraldo",
#     "sobrenome": "da Cruz"}
# null: é um valor especial que indica ausência de valor
#
# Ao converter de Python para JSON:
# Python        JSON
# dict          object
# list, tuple    array
# string        string
# int, float    number
# True          true
# False         false
# None          null
