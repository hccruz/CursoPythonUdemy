# Informe o seu nome
nome = 'Heraldo'
# Informe a sua altura
altura = 1.75
# Informe o seu peso
peso = 90

# Cálculo do IMC
imc = peso / altura ** 2

# Impressão no terminal do cálculo do IMC
print(f'{nome} tem {altura:.2f}m de altura, pesa {peso}kg ' +
      f'e seu IMC é {imc:.2f}.')
