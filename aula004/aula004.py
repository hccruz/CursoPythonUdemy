"""
Tipos de dados
str - string - textos 'Assim' "Assim"
int - inteiro - 123456 0 -10 -20 -50 -60 -1500 1500
float - real/ponto flutuante - 10.5 1.5 -10.10 -50.93 0.0
bool - booleano/lógico - True/False 10 == 10
"""
# String: nome
print('Heraldo', type('Heraldo'))

# Int: idade
print(49, type(49))

# Float: altura
print(1.77, type(1.77))

# Bool: É maior de idade
print(49 > 18, type(49 > 18))
