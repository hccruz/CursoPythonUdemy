"""
Operadores Aritméticos:
+, -, *, /, //, **, %, ()
"""
print(10 * 10)  # Multiplicação
print(10 + 10)  # Adição
print(10 - 5)  # Subtração
print(10 / 2)  # Divisão

print('Heraldo' * 10)  # Multiplicação de string
print('Heraldo' + ' ' + 'Cruz')  # Concatenação de strings

print(10 // 3)  # Divisão interia
print(10 ** 2)  # Potenciação
print(10 % 3)  # Resto da divisão
